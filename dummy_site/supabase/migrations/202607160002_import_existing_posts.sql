- **6분**: 노른자가 완전히 흐르는 반숙 (온천란에 가까운 질감)
- **8분**: 노른자 중심부만 촉촉하게 흐르는 반숙
- **10분**: 노른자 가장자리는 익고 중심부는 살짝 촉촉한 상태
- **12분 이상**: 노른자까지 완전히 단단해진 완숙

